package com.hapnium.core.enums;

/**
 * Defines the format type of the email content.
 * <ul>
 *   <li>{@link #HTML} - Represents an email with HTML content.</li>
 *   <li>{@link #TEXT} - Represents a plain text email.</li>
 * </ul>
 */
public enum MailType {
    /** Represents an email with HTML content. */
    HTML,

    /** Represents a plain text email. */
    TEXT,
}